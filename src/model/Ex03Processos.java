package model;

public class Ex03Processos {

	String nome;
	int qtdRetornos;

	public Ex03Processos(String nome, int qtdRetornos) {
		this.nome = nome;
		this.qtdRetornos = qtdRetornos;
	}

	public String getNome() {
		return nome;
	}

	public int getQtdRetornos() {
		return qtdRetornos;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public void setQtdRetornos(int qtdRetornos) {
		this.qtdRetornos = qtdRetornos;
	}
	
	
}
